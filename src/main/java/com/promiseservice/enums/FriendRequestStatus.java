package com.promiseservice.enums;

/**
 * 친구 요청의 상태를 정의하는 enum
 * 이유: 친구 요청의 생명주기를 명확하게 구분하여 요청 처리 과정을 추적하기 위해
 */
public enum FriendRequestStatus {
    
    /**
     * 대기 중 - 친구 요청이 보내진 상태 (스키마 PENDING에 맞춤)
     * 이유: 사용자가 친구 요청을 보냈지만 아직 응답하지 않은 상태를 표현
     */
    PENDING,
    
    /**
     * 수락됨 - 친구 요청이 수락된 상태
     * 이유: 친구 요청이 수락되어 실제 친구 관계가 형성된 상태를 표현
     */
    ACCEPTED,
    
    /**
     * 거절됨 - 친구 요청이 거절된 상태
     * 이유: 친구 요청이 거절되어 친구 관계가 형성되지 않은 상태를 표현
     */
    REJECTED
}




